/**Operator Perbandingan
 * Operator perbandingan adalah operator yang hasilnya adalah bool
 * Operator perbandingan bisa digunakan di tipe data Number dan String
 */

/**Lambang operator perbandingan
 * == Sama dengan
 * != Tidak sama dengan (negasi)
 * > lebih dari
 * < kurang dari
 * >= Lebih dari atau sama dengan
 * <= kurang dari atau sama dengan
 */

void main(List<String> args) {
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('Muflikhan' == 'Muflikhan');
  print('Muflikhan' != 'Muflikhan');
  
}
